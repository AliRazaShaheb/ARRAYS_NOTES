export const array_Clone_Slice = (arr) => {
  return arr.slice(0);
};

export const array_Clone_Map = (arr) => {
  return arr.map((item) => item);
};

export const array_Clone_Filter = (arr) => {
  return arr.filter(() => true);
};

export const array_Clone_Spread = (arr) => {
  return [...arr];
};
